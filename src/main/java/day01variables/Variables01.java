package day01variables;

public class Variables01 {


//Herhangi bir 3 öğenin fiyatları için double data tipinde değişkenler oluşturunuz. Fiyatların
    //  toplamını konsola yazdırınız

    public static void main(String[] args) {
        double elmaFiyat=15.99;
        double armutFiyat=22.99;
        double hurmaFiyat=18.99;

        System.out.println("Elma Fiyat = 15.99");
        System.out.println("Armut Fiyat = 22.99");
        System.out.println("Hurma Fiyat = 18.99");

        System.out.println("Toplam Fiyat = "+(elmaFiyat+armutFiyat+hurmaFiyat));
    }

}
